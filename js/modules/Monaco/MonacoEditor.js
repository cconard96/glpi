window.GLPI = window.GLPI || {};

export default class MonacoEditor {
    constructor(element_id, language) {
        const el = document.getElementById(element_id);
        window.monaco.editor.create(el, {
            value: el.value,
            language: language,
        });
    }
}

window.GLPI.MonacoEditor = MonacoEditor;
